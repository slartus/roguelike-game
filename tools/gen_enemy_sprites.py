"""
Генератор pixel-art спрайтов монстров для рогалика.

Классические RPG-типы: слизни, гоблины, орки, нежить (скелеты, зомби, личи),
пауки, некромант-босс. Все спрайты 16×16 (босс 32×32) рисуются построчно
через палитру + матрицу символов. Детерминированно.

Использование:
    python3 tools/gen_enemy_sprites.py

Правки: меняй PALETTE / MATRIX своего монстра, запускай скрипт, коммить
и PNG, и изменение скрипта. Не редактируй PNG вручную.
"""

from __future__ import annotations

from pathlib import Path

from PIL import Image

OUT_DIR = Path(__file__).resolve().parents[1] / "assets" / "sprites" / "enemies"


def render(rows: list[str], palette: dict[str, tuple[int, int, int, int]]) -> Image.Image:
    height = len(rows)
    width = max(len(row) for row in rows)
    img = Image.new("RGBA", (width, height), (0, 0, 0, 0))
    pixels = img.load()
    for y, row in enumerate(rows):
        padded = row.ljust(width, ".")
        for x, ch in enumerate(padded):
            color = palette.get(ch)
            if color is not None:
                pixels[x, y] = color
    return img


