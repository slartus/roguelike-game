#!/usr/bin/env python3
"""
Генератор декоративных спрайтов подземелья:
- mold.png             — крупный ярко-зелёный лишайник на стенах
- candle.png           — настенный канделябр с двумя свечами и огнём
- floor_crack.png      — трещина на плите пола
- floor_blood.png      — засохшее кровяное пятно с брызгами

Все декали рисуются поверх основной текстуры пола/стены как Sprite2D
в отдельном слое DecorRoot (см. floor.gd::_place_decor). Прозрачность
альфа-канала важна — сквозь плесень должен просвечивать кирпич.
"""

from __future__ import annotations

from pathlib import Path

from PIL import Image

OUT_DIR = Path(__file__).resolve().parents[1] / "assets" / "sprites" / "environment"


def render(rows: list[str], palette: dict[str, tuple[int, int, int, int]]) -> Image.Image:
    height = len(rows)
    width = max(len(row) for row in rows)
    img = Image.new("RGBA", (width, height), (0, 0, 0, 0))
    pixels = img.load()
    for y, row in enumerate(rows):
        padded = row.ljust(width, ".")
        for x, ch in enumerate(padded):
            color = palette.get(ch)
            if color is not None:
                pixels[x, y] = color
    return img


